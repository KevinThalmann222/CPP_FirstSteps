#include <iostream>

// Variable name rules:
// 1.) Can not start with a number
// 2.) Can not contain spaces
// 3.) Variable name must be unique

// Deklaration -> Variable: Zuweisen von Names und Typen zu ersten Mal. -> double my_var
// Initialisierung -> Zuweisen eines Wertes (Var "=" Wert)

int main()
{
    double my_var = 13.37; // Dek + Ini
    my_var = 24;           // Zuweisung

    return 0;
}
